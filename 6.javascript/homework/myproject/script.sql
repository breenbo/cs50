CREATE TABLE places (
    country_code CHAR(2),
    postal_code VARCHAR(20),
    place_name VARCHAR(180),
    admin_name1 VARCHAR(100),
    admin_code1 VARCHAR(20),
    admin_name2 VARCHAR(100),
    admin_code2 VARCHAR(20),
    admin_name3 VARCHAR(100),
    admin_code3 VARCHAR(20),
    latitude FLOAT,
    longitude FLOAT,
    accuracy INTEGER(1)
)
